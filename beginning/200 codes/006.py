# data type
int_data = 1 #integer
print(type(int_data))
print(int_data)

float_data = 3.14 #float
print(type(float_data))
print(float_data)

complex_data = 1 + 5j #complex
print(type(complex_data))
print(complex_data)

str_data1 = "I love python." #str
print(type(str_data1))
print(str_data1)

list_data = [1, 2, 3] #list
print(type(list_data))
print(list_data)

tuple_data = (1, 2, 3) #tuple
print(type(tuple_data))
print(tuple_data)

dict_data = {0: "False", 1: "True"} #dict
print(type(dict_data))
print(dict_data)
